// Simple keyword-based RAG search
export function retrieveRelevantChunks(query, topK = 3) {
  const q = query.toLowerCase();

  const scored = knowledgeBase.map(chunk => {
    let score = 0;

    // Score by keyword matches
    chunk.keywords.forEach(kw => {
      if (q.includes(kw.toLowerCase())) score += 2;
    });

    // Score by topic word matches
    const topicWords = chunk.topic.toLowerCase().split(' ');
    topicWords.forEach(word => {
      if (q.includes(word) && word.length > 3) score += 1;
    });

    // Score by content word matches (lighter weight)
    const contentWords = chunk.content.toLowerCase().split(/\s+/);
    contentWords.forEach(word => {
      if (word.length > 4 && q.includes(word)) score += 0.5;
    });

    return { ...chunk, score };
  });

  // Sort by score, return top K
  return scored
    .filter(c => c.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, topK);
}

